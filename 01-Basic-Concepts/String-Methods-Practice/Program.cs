string ifade = "Merhaba programlama dünyası.";
Console.WriteLine(ifade); //Prints the string to the console.
Console.WriteLine(ifade.Length); //Returns the total length of the string.
Console.WriteLine(ifade.ToUpper()); //Converts all characters to uppercase.
Console.WriteLine(ifade.ToLower());  //Converts all characters to lowercase.
Console.WriteLine(ifade.TrimStart()); //Removes leading whitespace from the start.
Console.WriteLine(ifade.TrimEnd());  //Removes trailing whitespace from the end. Use Trim() for both.
Console.WriteLine(ifade[0]); //Gets the character at index 0 (first character).
Console.WriteLine(ifade[1]); //Gets the character at index 1 (second character).
Console.WriteLine(ifade[^1]); // ^1 directly points to the 1st element from the end
Console.ReadKey();



